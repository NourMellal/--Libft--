#include "libft.h"

void	ft_strclr(char *s)
{
	int	i;
	int	slen;

	if (!s)
		return ;
	i = 0;
	slen = ft_strlen(s);
	while (s)
	{
		s[i] = '\0';
		if (i == slen)
			return ;
		i++;
	}
}
